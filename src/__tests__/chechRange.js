import sortHealthRank from '../index';

test('checking of correct setting healt rank', () => {
  const heroes = [
    { name: 'мечник', health: 10 },
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
  ];
  const expectHeroes = [
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
    { name: 'мечник', health: 10 },
  ];
  const result = sortHealthRank(heroes);

  expect(result).toEqual(expectHeroes);
});
